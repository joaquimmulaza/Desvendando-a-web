const mensagem = "Ola mundo"
const mensagem2 = "Hello world"

console.log(mensagem.split(' ')) // QUEBRA A STRING EM ARRAY

console.log(mensagem.length) // VER O TAMANHO DA STRING (CARACTERES)

console.log(mensagem.concat(mensagem2)) //CONCATENA UMA STRING COM OUTRA

console.log(mensagem + mensagem2) //CONCATENA UMA STRING COM OUTRA

console.log(`${mensagem} ${mensagem2}`) //CONCATENA UMA STRING COM OUTRA

console.log(mensagem.toLowerCase()) //COLOCA TODAS PALAVRAS EM MINUSCULA

console.log(mensagem.toUpperCase()) //COLOCA TODAS PALAVRAS EM MAIUSCULA

console.log(mensagem.replace('mundo', 'pessoal')) //TROCA UMA STRING POR OUTRA